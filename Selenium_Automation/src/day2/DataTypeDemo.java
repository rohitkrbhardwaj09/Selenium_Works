package day2;

public class DataTypeDemo {

	public static void main(String[] args) {
		//int a; //initialization
		//a = 100; //declaration
		
		int a = 100; //Initialization + declaration
		System.out.println("Welcome"); //Welcome
		System.out.println(10); //10
		System.out.println(a); //100

		//Variable in multiple lines
		//Approach 1
		int x1 = 100;
		int y1 = 200;
		int z1 = 300;
		
		System.out.println("The value of x1: "+x1);
		System.out.println("The value of y1: "+y1);
		System.out.println("The value of z1: "+z1);
		
		//Approach 2
		int x2, y2, z2;
		x2 = 400;
		y2 = 500;
		z2 = 600;
		
		System.out.println("The value of x2: "+x2);
		System.out.println("The value of y2: "+y2);
		System.out.println("The value of z2: "+z2);
	
		//Approach 3
		int x3 = 700;
		int y3 = 800;
		int z3 = 900;
		
		System.out.println("The value of x3: "+x3);
		System.out.println("The value of y3: "+y3);
		System.out.println("The value of z3: "+z3);

	}

}
